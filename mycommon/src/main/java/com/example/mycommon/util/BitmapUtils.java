    /**
     * 图片按比例大小压缩方法
     * @param originalBitmap （根据Bitmap图片压缩）
     * @return
     */
    public static Bitmap compressScale(Bitmap originalBitmap,int width) {
        int originalWidth = originalBitmap.getWidth();
        int originalHeight = originalBitmap.getHeight();

        float scale = ((float) width) / originalWidth;
        Matrix matrix = new Matrix();
        matrix.postScale(scale, scale);
        Bitmap changedBitmap = Bitmap.createBitmap(originalBitmap, 0, 0,
                originalWidth, originalHeight, matrix, true);

        return changedBitmap;
    }
